              пациентов симптоматической постуральной гипотензии, проявлявшейся
              в виде головокружений (без обморока). У отдельных чувствительных
              пациентов, получающих альфа-адреноблокаторы, одновременное
              применение силденафила может привести к симптоматической
              гипотензии.
            </p>
            <p>
              Признаков значительного взаимодействия с толбутамидом (250 мг) или
