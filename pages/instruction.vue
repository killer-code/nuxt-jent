            <p>
              <span class="orange"
                >Влияние других лекарственных препаратов на фармакокинетику
                силденафила</span
              >
            </p>
            <p>
              Метаболизм силденфила происходит в основном под действием
              изоферментов цитохрома CYP3А4 (основной путь) и CYP2С9 (минорный
              путь), поэтому ингибиторы этих изоферментов могут уменьшить
              клиренс силденафила, а индукторы, соответственно, увеличить
              клиренс силденафила. Отмечено снижение клиренса силденафила при
              одновременном применении ингибиторов изофермента цитохрома CYP3А4
              (кетоконазол, эритромицин, циметидин).
            </p>
            <p>
              Циметидин (800 мг), неспецифический ингибитор изофермента
              цитохрома CYP3А4, при совместном приеме с силденафилом (50 мг)
              вызывает повышение концентрации силденафила в плазме на 56 %.
              Однократный прием 100 мг силденафила совместно с эритромицином (по
              500 мг/сутки 2 раза в день в течение 5 дней), умеренным
              ингибитором изофермента цитохрома CYP3А4, на фоне достижения
              постоянной концентрации эритромицина в крови, приводит к
              увеличению AUC силденафила на 182 %. При совместном приеме
              силденафила (однократно 100 мг) и саквинавира (1200 мг/день 3 раза
              в день), ингибитора ВИЧ-протеазы и изофермента цитохрома CYP3А4,
              на фоне достижения постоянной концентрации саквинавира в крови С
              max силденафила повышалась на 140 %, а AUC увеличивалась на 210 %.
              Силденафил не оказывает влияния на фармакокинетику саквинавира.
              Более сильные ингибиторы изофермента цитохрома CYP3А4, такие как
              кетоконазол и итраконазол, могут вызывать и более сильные
              изменения фармакокинетики силденафила.
            </p>
            <p>
              Одновременное применение силденафила (однократно 100 мг) и
              ритонавира (по 500 мг 2 раза в сутки), ингибитора ВИЧ-протеазы и
              сильного ингибитора цитохрома Р 450 , на фоне достижения
              постоянной концентрации ритонавира в крови приводит к увеличению С
              max силденафила на 300 % (в 4 раза), а AUC на 1000 % (в 11 раз).
              Через 24 часа концентрация силденафила в плазме крови составляет
              около 200 нг/мл (после однократного применения одного силденафила
              – 5 нг/мл). Это согласуется с эффектом ритонавира на широкий
              диапазон субстратов цитохрома Р 450 . Силденафил не влияет на
              фармакокинетику ритонавира. Учитывая эти данные, одновременный
              прием ритонавира и силденафила не рекомендуется. В любом случае
              максимальная доза силденафила ни при каких обстоятельствах не
              должна превышать 25 мг в течение 48 часов.
            </p>
            <p>
              Если силденафил принимают в рекомендуемых дозах пациенты,
              получающие одновременно сильные ингибиторы изофермента цитохрома
              CYP3А4, то С max свободного силденафила не превышает 200 нМ, и
              препарат хорошо переносится. Однократный прием антацида (магния
              гидроксида/алюминия гидроксида) не влияет на биодоступность
              силденафила.
            </p>
            <p>
              В исследованиях с участием здоровых добровольцев при одновременном
              применении антагониста эндотелиновых рецепторов, бозентана
              (индуктор изофермента CYP3А4 (умеренный), CYP2С9 и, возможно,
              CYP2С19) в равновесной концентрации (125 мг 2 раза в сутки) и
              силденафила в равновесной концентрации (80 мг 3 раза в сутки)
              отмечалось снижение AUC и С max силденафила на 62,6 % и 52,4 %,
              соответственно. Силденафил увеличивал AUC и С max бозентана на
              49,8 % и 42 %, соответственно. Предполагается, что одновременное
              применение силденафила с мощными индукторами изофермента CYP3А4,
              такими как рифампицин, может приводить к большему снижению
              концентрации силденафила в плазме крови.
            </p>
            <p>
              Ингибиторы изофермента цитохрома CYP2С9 (толбутамид, варфарин),
              изофермента цитохрома CYP2D6 (селективные ингибиторы обратного
              захвата серотонина, трициклические антидепрессанты), тиазидные и
              тиазидоподобные диуретики, ингибиторы АПФ и антагонисты кальция не
              оказывают влияния на фармакокинетику силденафила. Азитромицин (500
              мг/сут в течение 3 дней) не оказывает влияния на AUC, C max , T
              max , константу скорости выведения и Т 1/2 силденафила или его
              основного циркулирующего метаболита.
            </p>
            <p>
              <span class="orange">
                Влияние силденафила на другие лекарственные средства
              </span>
            </p>
            <p>
              Силденафил является слабым ингибитором изоферментов цитохрома Р
              450 – 1А2, 2С9, 2С19, 2D6, 2Е1 и 3А4 (ингибирующая молярная
              концентрация ИК 50 &gt; 150 мкмоль). При приеме силденафила в
              рекомендуемых дозах его C max составляет около 1 мкмоль, поэтому
              маловероятно, что силденафил может повлиять на клиренс субстратов
              этих изоферментов. Силденафил усиливает гипотензивное действие
              нитратов как при длительном применении последних, так и при их
              назначении по острым показаниям. В связи с этим, применение
              силденафила в сочетании с нитратами или донаторами оксида азота
              противопоказано.
