              сердечно-сосудистых осложнений. Многие из указанных нежелательных
              явлений наблюдались вскоре после сексуальной активности, и
              некоторые из них отмечались после приема силденафила без
              последующей сексуальной активности. Не представляется возможным
              установить наличие прямой связи между отмечавшимися нежелательными
              явлениями и указанными или иными факторами.
