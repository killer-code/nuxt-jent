            <p>
              <span class="orange">
                Нарушения зрения
              </span>
            </p>
            <p>
              В редких случаях во время пострегистрационного применения всех
              ингибиторов ФДЭ5, в том числе силденафила, сообщалось о
              неартериитной передней ишемической невропатии зрительного нерва
              (НПИНЗН) – редком заболевании и причине снижения или потери
              зрения. У большинства из этих пациентов были факторы риска, в
              частности снижение отношения диаметров экскавации и диска
              зрительного нерва («застойный диск»), возраст старше 50 лет,
              сахарный диабет, гипертензия, ишемическая болезнь сердца,
              гиперлипидемия и курение. В обсервационном исследовании
              оценивалась связь недавнего применения препаратов класса
              ингибиторов ФДЭ5 с острым началом НПИНЗН. Результаты указывают на
              приблизительно 2-кратное повышение риска НПИНЗН в пределах 5
              периодов полувыведения после применения ингибитора ФДЭ5. Согласно
              опубликованным литературным данным, годичная частота возникновения
              НПИНЗН составляет 2,5-11,8 случаев на 100 000 мужчин в возрасте ≥
              50 лет в общей популяции. Следует рекомендовать пациентам в случае
              внезапной потери зрения прекратить терапию силденафилом и
              немедленно проконсультироваться с врачом. Лица, у которых уже был
              случай НПИНЗН, имеют повышенный риск рецидива НПИНЗН. Поэтому
              врачу следует обсудить данный риск с такими пациентами, а также
              обсудить с ними потенциальный риск неблагоприятного воздействия
              ингибиторов ФДЭ5. Ингибиторы ФДЭ5, в том числе силденафил, у таких
              пациентов следует применять с осторожностью и только в ситуациях,
              когда ожидаемая польза превышает риск.
